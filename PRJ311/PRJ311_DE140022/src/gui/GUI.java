    private void loadTable() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        allStores = StoreDB.getAll();
        for (Store s : allStores) {
            dtm.addRow(new Object[]{
                s.getStoreID(), s.getStoreName(), s.getAddress(), s.getPhone()
            });
        }
    }

