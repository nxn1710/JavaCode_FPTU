    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String id = tfID.getText().trim();
        String name = tfName.getText().trim();
        String address = tfAddress.getText().trim();
        String phone = tfPhone.getText().trim();
        if (!id.matches("^ST\\d{2}$")) {
            JOptionPane.showMessageDialog(rootPane, "ID must be in format STxx");
        } else if (StoreDB.getStore(id) != null) {
            JOptionPane.showMessageDialog(rootPane, "Store cann't duplicate");
        } else if (!phone.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(rootPane, "Phone must be 10 digit");
        } else {
            if (StoreDB.newStore(new Store(id, name, address, phone))) {
                loadTable();
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tbStoreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbStoreMouseClicked
        int index = tbStore.getSelectedRow();
        tfID.setText(dtm.getValueAt(index, 0).toString().trim());
        tfName.setText(dtm.getValueAt(index, 1).toString().trim());
        tfAddress.setText(dtm.getValueAt(index, 2).toString().trim());
        tfPhone.setText(dtm.getValueAt(index, 3).toString().trim());
    }//GEN-LAST:event_tbStoreMouseClicked

