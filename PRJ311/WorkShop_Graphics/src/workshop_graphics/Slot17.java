    private void showProperties() {
        properties.setText("");
        properties.append("Font family: " + font.getFamily() + "\n");
        properties.append("Font Name: " + font.getName() + "\n");
        properties.append("Font Style: " + font.getStyle() + "\n");
        properties.append("Font Size: " + font.getSize() + "\n");
        FontMetrics fm = g.getFontMetrics();
        properties.append("Font Metrics\n");
        properties.append("Ascent: " + fm.getAscent() + "\n");
        properties.append("Max Ascent: " + fm.getMaxAscent() + "\n");
        properties.append("Desent:  " + fm.getDescent() + "\n");
        properties.append("Desent:  " + font.getSize() + "\n");
        properties.append("Leading:  " + fm.getLeading() + "\n");
        properties.append("Height:  " + fm.getHeight() + "\n");
        properties.append("Max advance:  " + fm.getMaxAdvance() + "\n");
        if (text.length() > 0) {
            properties.append("Strign width: " + fm.stringWidth(text));
        }

    }

    private void cbStyleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbStyleActionPerformed
        String styleName = cbStyle.getSelectedItem().toString();
        if (styleName.equalsIgnoreCase("bold")) {
            style = Font.BOLD;
        } else if (styleName.equalsIgnoreCase("plain")) {
            style = Font.PLAIN;
        } else {
            style = Font.ITALIC;
        }
        font = new Font(fontName, style, fontSize);
        showProperties();
    }//GEN-LAST:event_cbStyleActionPerformed

    private void tfSizeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfSizeFocusLost
        fontSize = Integer.parseInt(tfSize.getText());
        font = new Font(fontName, style, fontSize);
        g.setFont(font);
        showProperties();

    }//GEN-LAST:event_tfSizeFocusLost

