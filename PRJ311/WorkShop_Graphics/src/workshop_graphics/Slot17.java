    private void pnDrawMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnDrawMouseClicked
        g.setFont(font);
        g.setColor(color);
        int x = evt.getX();
        int y = evt.getY();
        text = tfText.getText();
        g.drawString(text, x, y);
    }//GEN-LAST:event_pnDrawMouseClicked

